function handleInput(){
    let userInput = $(".search-input").val();
    let isValidColor = false;
    let isHex = false;
    if(colorList.some(color => color.toLowerCase() === userInput.toLowerCase().replace('#', ''))) {
        isValidColor = true;
    }
    if(userInput.charAt(0)!=="#"&&isValidColor===false){
        userInput = "#"+userInput;
    }
    if(/^#[0-9a-fA-F]{3}$|^#[0-9a-fA-F]{6}$/.test(userInput)){
        isHex = true;
    }
    if(isHex===true||isValidColor===true){
        $(".result-box").css("background-color",userInput);
    }
    else{
        $(".search-input").val('');
        $(".search-input").attr("placeholder","Enter a valid input");
        setTimeout(function(){
            $(".search-input").attr("placeholder","Type hex or color name");
        }, 4000);
    }
}

$(".search-button").on("click",function(){
    handleInput();
});


$(".search-input").keydown(function(e){
    if(e.keyCode===13){
        e.preventDefault();
        handleInput();
    }
})

